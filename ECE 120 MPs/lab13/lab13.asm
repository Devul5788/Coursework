; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.

;intro para
;To solve this problem, I modified a majority of my code 
;from the last lab to essentially enable line by line printing
;of a string. I did this by adding another loop that 
;finds the current column's character and then calculates
;the relevant font data for it. It then uses this font data
;to print the char for 0 and the char for 1 as defined by the font 
;data for each character in the string.


; Register Table
; R0 <-- output ascii character
; R1 <-- M[x5000] (0)
; R2 <-- M[x5001] (1)
; R3 <-- memory address of the current char, currently accessed font data
; R4 <-- Memory address for relevant font data
; R5 <-- Row Counter
; R6 <-- Column Counter 

		.ORIG x3000
		AND R0, R0, #0		;clears R0
		LDI R1, ZERO_CHAR	;loads and sets R1 to the char that replaces 0
		LDI R2, ONE_CHAR	;loads and sets R2 to the char that replaces 1
		LDI R3, CURRENT_CHAR	;loads the first character of the string into R3
		LEA R4, FONT_DATA	;set R4 to the position at which font data starts
		AND R5, R5, #0		;clear R5
		ADD R5, R5, #15		;set R5 to 15
		
				
		
FONT_ADR_LOOP   			;finds the address of the relevant font data by 16*(ascii char)
		ADD R3, R3, #0		
		BRz ROW_LOOP
		ADD R4, R4, #15
		ADD R4, R4, #1	
		ADD R3, R3, #-1
		BRnzp FONT_ADR_LOOP
				
				
ROW_LOOP				;Loop 1 - loops over all relevant 16 rows, and sets col counter
		ADD R5, R5, #0
		BRn DONE
		
		LDI R3, CURRENT_CHAR	;special branch for null string character
		BRz DONE_ROW
		
		AND R6, R6, #0
		ADD R6, R6, #7
		NOT R0, R5		;Finds the appropriate memory address based on the current line printing
		ADD R0, R0, #1		;This is done by R4 = R4 + (15 - R5)
		ADD R0, R0, #15
		ADD R4, R4, R0
		LDR R3, R4, #0		;Once the correct memory addres has been found, set R3 to the font data at that memory address
		
		
		
		BRnzp COL_CHAR_LOOP 
				
COL_CHAR_LOOP  				;Loop 2 - loops over 8 cols, prints certain ascii when font data is 0 and branches when it is 1
		ADD R6, R6, #0
		BRn NEXT_CHAR_LOOP	;if all cols in the current line is done, move to the next char in the line			
		ADD R3, R3, 0
		BRn ONE_CHAR_OUT	;Conditional statement for printing 1 if the font data is 1, and 0 if it is 0
		ADD R0, R1, #0
		OUT
		ADD R3, R3, R3		;left shifts R3
		ADD R6, R6, #-1		;decrements column counter
		BRnzp COL_CHAR_LOOP
		
ONE_CHAR_OUT 			        ;print the relevant chracter when the font data is 1
		ADD R0, R2, #0
		OUT
		ADD R3, R3, R3	
		ADD R6, R6, #-1
		BRnzp COL_CHAR_LOOP


DONE_ROW
		LD R0, LINEFEED         ;prints linefeed
		OUT			
		LD R4, STRING_START 	;sets CURRENT_CHAR back to the first character for the new line
		ST R4, CURRENT_CHAR
		LDR R3, R4, #0	      	;sets R3 back to the address of the current char
		ADD R5, R5, #-1	      	;decrements row counter
		LEA R4, FONT_DATA     	;Recalculates the relevant font data
		BRnzp FONT_ADR_LOOP


NEXT_CHAR_LOOP 	
		LD R4, CURRENT_CHAR  	;calculates the next character in the string
		ADD R4, R4, #1
		ST R4, CURRENT_CHAR	;sets the current char to that character
		LDR R3, R4, #0		
		BRz DONE_ROW		;if null is found it ends the program
		LEA R4, FONT_DATA	;recalculates relevant memory address
		BRnzp FONT_ADR_LOOP
		

DONE		TRAP x25
LINEFEED 	.FILL x0A
ZERO_CHAR	.FILL x5000
ONE_CHAR	.FILL x5001
STRING_START	.FILL x5002
CURRENT_CHAR	.FILL x5002


